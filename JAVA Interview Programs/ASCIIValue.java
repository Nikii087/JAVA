class ASCIIValue
{
    public static void main(String args[])
    {
        // character whose ASCII value to be found  
        char ch1='a';
        char ch2='b';
        // variable that stores the integer value of the character  
        int asciival1=ch1;
        int asciival2=ch2;

        System.out.println("The ascii value of "+ch1 +" is "+ asciival1);
        System.out.println("The ascii value of "+ch2 +" is "+ asciival2);



        
    }
    
}